let plato = parseInt(prompt("Seleccione su plato"))

switch (plato) {
    case 1:
        alert("Seleccionó el plato número 1")
        break
    case 2:
        alert("Seleccionó el plato número 2")
        break
    case 3:
        alert("Seleccionó el plato número 3")
        break
    default:
        alert("Número inválido")
}